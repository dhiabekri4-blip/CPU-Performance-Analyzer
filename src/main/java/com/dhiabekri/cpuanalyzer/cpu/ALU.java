package com.dhiabekri.cpuanalyzer.cpu;

public class ALU {
    public int add(int a, int b) {
        return a + b;
    }
    public int sub(int a,int b){
        return a-b;
    }
    public int multiply(int a,int b){
        return a*b;
    }
    public int and(int a,int b){
        return a&b;
    }
    public int or(int a,int b){
        return a|b;
    }

}
